package com.rats.models;

public class Position {

        private Long x;
        private Long y;

        public Position() {
        }

        public Long getX() {
            return x;
        }

        public void setX(Long x) {
            this.x = x;
        }

        public Long getY() {
            return y;
        }

        public void setY(Long y) {
            this.y = y;
        }
}
